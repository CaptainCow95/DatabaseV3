namespace DatabaseV3
{
    /// <summary>
    /// Represents the different logging levels.
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// An error message.
        /// </summary>
        Error,

        /// <summary>
        /// A warning message.
        /// </summary>
        Warning,

        /// <summary>
        /// An info message.
        /// </summary>
        Info,

        /// <summary>
        /// A debug message.
        /// </summary>
        Debug
    }
}